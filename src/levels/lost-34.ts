async function onLevelStart() {
  // HideLevel
  for (let x = 0; x <= XMax; x++) {
    for (let y = 0; y <= YMax; y++) {
      const e = world.level.map.get(x, y)!;
      if (e) {
        world.level.map.set(
          x,
          y,
          new Entity(e.type, { x, y, ch: e.ch, fg: null, bg: null }),
        );
      }
    }
  }
}

