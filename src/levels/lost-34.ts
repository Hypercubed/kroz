async function onLevelStart() {
  // HideLevel
  for (let x = 0; x <= XSize; x++) {
    for (let y = 0; y <= YSize; y++) {
      const e = state.level.map.get(x, y)!;
      if (e) {
        state.level.map.set(x, y, new Entity(e.type, e.ch, null, null));
      }
    }
  }
}

